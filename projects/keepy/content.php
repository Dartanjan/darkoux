    <div class="row">
      <h2 class="text-center">Challenges</h2>
      <div class="col-xs-12">
        <h3>Understanding data</h3>
        <p>Although the app utilized Mixpanel analytics, at times it was very difficult, even impossible to come to clear conclusion on whether the (re)design made positive or negative changes. This is not pleasant as it does not lead to validated learning and gives too much room to subjective decision-making.</p>
        <h3>Balancing the onboarding process</h3>
        <p>Onboarding is difficult to crack. A concrete challenge was to balance between getting the customer to the main screen as soon as possible &ndash; so that they experience the app themselves &ndash; and taking them by hand through several steps to make sure they create their first "keepy." For me, the first method felt better, but more difficult to execute right (requires that UVP is both very attractive and is clearly communicated). The second method ensures they create their first keepy, which in theory means they should experience the UVP sooner, but it could also create a false sense of engagement: customer completed a goal only because we made him do it, not because he understood the value and expressed genuine interest. </p>
        <p>This juggling between less intense onboarding, hinting at what the UVP is and what the next steps are, and making sure the customer goes through key processes, is a classical onboarding challenge. The existing complex onboarding user flow, and the expectations around it, only made it a bit more tricky.</p>
      </div>
