    <div class="row">
      <h2 class="text-center">Challenges</h2>
      <div class="col-xs-12">
        <h3>Two sides of payment areas</h3>
        <p>What often makes payment projects more difficult to work on, especially with startups who have not yet reached the product/market fit, is that they inevitably ask the tough questions: have you cracked the revenue model yet? Refactoring information architecture, refreshing visual and UI design and rewriting copy only gets you to a certain point. It's all worthy optimization, but most startups need to focus on big experiments, not optimizations.</p>
        <p>While I think great UX can do wonders, sooner or later a couple of questions take project to a new direction: Do customers really need your product enough to pay; Is your revenue model appropriate for the goods/services you're selling to this customer segment; Are you overcharging? It all makes these projects particularly difficult for the team.</p>
